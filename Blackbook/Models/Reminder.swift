import Foundation
import SwiftData

@Model
final class Reminder {
    var id: UUID
    var contact: Contact?
    var title: String
    var dueDate: Date
    var isCompleted: Bool
    var recurrence: Recurrence?
    var isAutoGenerated: Bool
    var createdAt: Date

    var syncStatus: String = SyncStatus.pending.rawValue
    var lastSyncedAt: Date?

    var isOverdue: Bool {
        !isCompleted && dueDate < Date()
    }

    init(
        contact: Contact,
        title: String,
        dueDate: Date,
        recurrence: Recurrence? = nil,
        isAutoGenerated: Bool = false
    ) {
        self.id = UUID()
        self.contact = contact
        self.title = title
        self.dueDate = dueDate
        self.isCompleted = false
        self.recurrence = recurrence
        self.isAutoGenerated = isAutoGenerated
        self.createdAt = Date()
    }

    func nextOccurrence() -> Date? {
        guard let recurrence else { return nil }
        let calendar = Calendar.current
        switch recurrence {
        case .weekly:
            return calendar.date(byAdding: .weekOfYear, value: 1, to: dueDate)
        case .biweekly:
            return calendar.date(byAdding: .weekOfYear, value: 2, to: dueDate)
        case .monthly:
            return calendar.date(byAdding: .month, value: 1, to: dueDate)
        case .quarterly:
            return calendar.date(byAdding: .month, value: 3, to: dueDate)
        }
    }
}

enum Recurrence: String, Codable, CaseIterable, Identifiable {
    case weekly = "Weekly"
    case biweekly = "Biweekly"
    case monthly = "Monthly"
    case quarterly = "Quarterly"

    var id: String { rawValue }
}
